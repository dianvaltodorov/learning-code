# Процедурата да приканва потребителя, докато не той/тя/то не въведе числов низ с дължина над 5 символа.
#m Изведете цифрата на стотиците на стандартния изход с подходящи съобщение.
echo "Enter a number"
read input
re='?[0-9]+([.][0-9]+)?$'

length=${#input}

while [ $length -le 5 ] || [ $input =~ $re ]
do
    echo "Error"
    read input
done
